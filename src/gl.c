#ifdef LDK_OS_WINDOWS
#define LDK_GETGLPROC(param) ldkWin32OpenglProcAddressGet((char*)#param)

void* ldkWin32OpenglProcAddressGet(char* name)
{
  static HMODULE win32Opengl32Dll_ = NULL;
  if (!win32Opengl32Dll_ )
    win32Opengl32Dll_  = GetModuleHandleA("OpenGL32.dll");

  void* addr = (void*) GetProcAddress(win32Opengl32Dll_ , name);
  if(! addr)
    addr = (void*) wglGetProcAddress(name);
  return addr;
}
#endif  //LDK_OS_WINDOWS

